package com.company;

public class Cwiczenia_5 {

    public static void main(String[] args) {

        // Tabliczka mno≈ºenia.
        for (int i = 1; i <= 10; i++) {
            for (int k = 1; k <= 10; k++) {
                System.out.println(i + " * " + k + " = " + i * k);
            }
            System.out.println("-----");
        }
    }
}
